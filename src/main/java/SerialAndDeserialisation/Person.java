package SerialAndDeserialisation;

public class Person {

	String name;
	int age;
	String ClassNameJi;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}

	public String getClassName()
	{
		return ClassNameJi;
	}
	
	public void setClassName(String ClassNameJi)
	{
		this.ClassNameJi=ClassNameJi;
	}

}
